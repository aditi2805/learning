package learning.basics;

public class EscapeSequence {
    public static void main(String[] args) {
        System.out.println("Hello World!\nMy name is Aditi.");
        for(int i = 1;i<=10;i++){
            System.out.println(17*i + "\t=\t" + 17+"\t*\t" + i );
        }
        for(int i = 1;i<=10;i++){
            System.out.println(18*i + "    =        " + 18+"    *    " + i );

        }

    }
}
